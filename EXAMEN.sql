-- Table: AGENCIA
CREATE TABLE AGENCIA (
    IDAGEN integer  NOT NULL,
    RUC_AGEN char(11)  NOT NULL,
    DIREC_AGEN varchar2(30)  NOT NULL,
    CONSTRAINT AGENCIA_pk PRIMARY KEY (IDAGEN)
) ;

-- Table: ALQUILER
CREATE TABLE ALQUILER (
    IDALQUI integer  NOT NULL,
    IDINQ integer  NOT NULL,
    FECH_INI_ALQUI date  NOT NULL,
    FECH_FIN_ALQUI date  NOT NULL,
    IMPO_ALQIU integer  NOT NULL,
    FIAN_ALQUI integer  NOT NULL,
    FECH_FIRM_ALQUI date  NOT NULL,
    ID_VIVI integer  NOT NULL,
    RENOV_ALQUI integer  NOT NULL,
    CONSTRAINT ALQUILER_pk PRIMARY KEY (IDALQUI)
) ;

-- Table: PERSONA
CREATE TABLE PERSONA (
    IDPER integer  NOT NULL,
    NOM_PER varchar2(30)  NOT NULL,
    APE_PER varchar2(30)  NOT NULL,
    DNI_PER char(8)  NOT NULL,
    TELF_PER char(9)  NOT NULL,
    DIREC_PER varchar2(30)  NULL,
    EMIAL_PER varchar2(50)  NULL,
    TIP_PER char(1)  NOT NULL,
    FECH_NAC_PER date  NULL,
    CONSTRAINT PERSONA_pk PRIMARY KEY (IDPER)
) ;

-- Table: VIVIENDA
CREATE TABLE VIVIENDA (
    IDVIVI integer  NOT NULL,
    CALL_VIVI varchar2(30)  NOT NULL,
    NUM_VIVI integer  NOT NULL,
    COD_POS_VIVI integer  NOT NULL,
    PISO_VIVI integer  NOT NULL,
    POBL_VIVI varchar2(30)  NOT NULL,
    DESC_VIVI varchar2(500)  NOT NULL,
    IDA_GEN integer  NOT NULL,
    IDPRO integer  NOT NULL,
    CONSTRAINT VIVIENDA_pk PRIMARY KEY (IDVIVI)
) ;

